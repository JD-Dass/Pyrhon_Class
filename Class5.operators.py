# operators
# Operators are simbols used to perform operations on variables and valus.
# in python , operators help us calculate , compare , assogn , and check conditions

# Arithmetic operators
# use in mathematic operations

# +, -, *, /, //, **, %
# + => Addition
# - => Subtraction
# * => Multiplication
# / => division
# ** => power
# // => floor division
#  % => modulus

# a = 10
# b = 3
# c =a // b
# print(c)

# Assignment Operators
# used to assign value to variables

# =, +=, -=, *=, /=, %=, **=, //=
# = => assign only 
# += => add and assign
# -= => subtract and assign
# *= => multiply and assign
# /= => divide and assign

# Comparison operator
# used to compare to values
# result will be true or false

# ==, !=, <, >, <=, >=
# == => Equal to
# != => not equal to
# <= => less then or equal to
# >= => greater then or equal to
# <  => less then
#  > => greater then

# Logical operators
# used to combine conditions

# and , or, not
# and => both true
# or => any one true
# not => reverse result

# Membership operators
# used to check values inside a collection

# in => "a" in "apple"
# not in => "b" not in "apple"

# identity Operators
# used to compare memory location ( same object or not)

# is => same object
# is not => different object

# bitwise operator
# bitwise operators works on binary numbers(0s and 1s)
# they operate bit by bit , so they are used in low level
#  programing , performance optimization, flags and networking concepts

